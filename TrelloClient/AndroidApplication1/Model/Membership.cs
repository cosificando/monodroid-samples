namespace AndroidApplication1.Model
{
    /// <summary>
    /// Represents a membership
    /// </summary>
    public class Membership
    {
        /// <summary>
        /// Gets or sets the id.
        /// </summary>
        /// <value>
        /// The id.
        /// </value>
        public string Id { get; set; }

        /// <summary>
        /// Gets or sets the id memeber.
        /// </summary>
        /// <value>
        /// The id memeber.
        /// </value>
        public string IdMemeber { get; set; }

        /// <summary>
        /// Gets or sets the type of the member.
        /// </summary>
        /// <value>
        /// The type of the member.
        /// </value>
        public string MemberType { get; set; }
    }
}